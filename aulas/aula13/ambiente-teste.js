/** Aula 13
 *  -> while
 *  -> do while
 */

var count = 1

while (count <= 3) {
    console.log(`Passo while ${count}`)
    count++
}

do {
    console.log(`Passo do while ${count}`)
    count--
} while (count >= 1)

